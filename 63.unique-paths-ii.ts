// @lc code=start
function uniquePathsWithObstacles(obstacleGrid: number[][]): number {

  // m行n列
  const m = obstacleGrid.length;
  const n = obstacleGrid[0].length;

  // [x,y,direction]
  // direction=-1未走过,0向右走过，1向下走过
  const stack: Array<Array<number>> = [];
  stack.push([0, 0, -1]);
  let result: number = 0;
  while (stack.length > 0) {
    const latest = stack[stack.length - 1];
    if (latest[0] === n || latest[1] === m) {
      // 碰壁了
      stack.pop();
    } else if (obstacleGrid[latest[1]][latest[0]] === 1) {
      // 当前是障碍
      stack.pop();
    } else if (latest[0] === n - 1 && latest[1] === m - 1) {
      // 到达终点
      result += 1;
      stack.pop();
    } else  if (latest[2] === -1) {
      // 向右走
      latest[2] = 0;
      const next = [latest[0] + 1, latest[1], -1];
      stack.push(next);
    } else if (latest[2] === 0) {
      // 向下走
      latest[2] = 1;
      const next = [latest[0], latest[1] + 1, -1];
      stack.push(next);
    } else {
      // 走完了，回退
      stack.pop();
    }
  }

  return result;
};
